function getBaseURL() {
    var l = location.href;
    var k = l.substring(0, l.indexOf("/", 14));
    if (k.indexOf("http://localhost") != -1) {
        var l = location.href;
        var j = location.pathname;
        var g = l.indexOf(j);
        var h = l.indexOf("/", g + 1);
        var i = l.substr(0, h);
        return i + "/"
    } else {
        return k + "/"
    }
}